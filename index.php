				India has a universal healthcare model that is mostly administered at the state level rather than the
				federal level. The public system is essentially free for all Indian residents except for small, often
				symbolic co-payments in some services. In 2019, the total net government spending on healthcare was 36
				billion or 1.23 % of its GDP. The Indian Constitution makes the provision of healthcare in India the
				responsibility of the state governments, rather than the central federal government. It makes every
				state responsible for "raising the level of nutrition and the standard of living of its people and the
				improvement of public health as among its primary duties".Since the country's independence, the public
				healthcare system has been entirely funded through general taxation.
			</p>
			<br />
			<p>
				The National Health Policy was endorsed by the Parliament of India in 1983 and updated in 2002, and then
				again updated in 2017. The recent four main updates in 2017 mentions the need to focus on the growing
				burden of non-communicable diseases, on the emergence of the robust healthcare industry, on growing
				incidences of unsustainable expenditure due to health care costs and on rising economic growth enabling
				enhanced fiscal capacity.
			</p>
			<br />
			<p>
				In practice however, the private healthcare sector is responsible for the majority of healthcare in
				India, and most healthcare expenses are paid directly out of pocket by patients and their families,
				rather than through health insurance. Government health policy has thus far largely encouraged
				private-sector expansion in conjunction with well designed but limited public health programmes.
			</p>
			<br />
			<p>
				A government-funded health insurance project was launched in 2018 by the Government of India, called
				Ayushman Bharat. This aimed to cover the bottom 50% of the country's population and offer them free
				treatment even at private hospitals.
			</p>
